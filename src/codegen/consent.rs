#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_parse_default_state() {
        assert_eq!(parse_default_state("opt-in"), ConsentState::OptIn);
        assert_eq!(parse_default_state("opt-out"), ConsentState::OptOut);
        assert_eq!(parse_default_state("unknown"), ConsentState::Pending);
    }

    #[test]
    fn test_sanitise_filename() {
        assert_eq!(sanitise_filename("src/app.js"), "src_app_js");
        assert_eq!(sanitise_filename("./index.html"), "index_html");
    }

    #[test]
    fn test_sanitise_identifier() {
        assert_eq!(sanitise_identifier("analytics_pageview_0"), "analytics_pageview_0");
        assert_eq!(sanitise_identifier("gate-name.test"), "gate_name_test");
    }

    #[test]
    fn test_capitalise() {
        assert_eq!(capitalise("analytics"), "Analytics");
        assert_eq!(capitalise(""), "");
    }

    #[test]
    fn test_generate_consent_manager_contains_categories() {
        let manifest = crate::manifest::Manifest {
            project: crate::manifest::ProjectConfig {
                name: "test".to_string(),
                source_root: ".".to_string(),
            },
            consent: crate::manifest::ConsentConfig {
                gdpr: true,
                ccpa: false,
                default_state: "opt-out".to_string(),
                categories: vec!["analytics".to_string(), "marketing".to_string()],
            },
            accessibility: Default::default(),
            i18n: crate::manifest::I18nConfig {
                default_locale: "en-GB".to_string(),
                supported_locales: vec!["en-GB".to_string()],
                extract_strings: true,
            },
            report: Default::default(),
        };
        let gates = vec![];
        let code = generate_consent_manager(&manifest, &gates);
        assert!(code.contains("\"analytics\""));
        assert!(code.contains("\"marketing\""));
        assert!(code.contains("\"opted_out\""));
        assert!(code.contains("class ConsentManager"));
    }
}
